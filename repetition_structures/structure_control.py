frutas = ["Maca", "Abacate", "Abacaxi"]

print("Percorrendo lista:")
for fruta in frutas: # A variável criada no laço for recebe o valor da lista
    print(fruta)

print("Laco for")
for numero in range(1, 6): #Lembrar que no "in range", ele percorre a "distância" e ai até um número antes
    print(numero)

print("Laco while")
contador = 0
while contador <= 5: # Especifica menor ou igual para ir até o número 5
    print(contador)
    contador += 1