fruits = ["maca", "banana", "abacaxi"]

print(fruits)

print(fruits[0]) # Irá printar maca
print(fruits[1]) # Irá printar banana
print(fruits[2]) # Irá printar abacaxi

# É possivel printar de forma inversa, utilizando "-"

print(fruits[-1]) # printa abacaxi, sendo "-1" a partir do último elemento
print(fruits[-2]) # printa banana
print(fruits[-3]) # printa maca

